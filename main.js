function solution(grid) {
  // Horizontally
  for (let i = 0; i < grid.length; i++) {
    let arr = grid[i];
    for (let t = 0; t < arr.length - 1; t++) {
      for (let j = t + 1; j < arr.length; j++) {
        if (arr[t] !== "." && arr[j] !== "." && arr[t] === arr[j]) {
          return false;
        }
      }
      // Vertically
      for (let j = i + 1; j < grid[i].length; j++) {
        if (
          grid[i][t] !== "." &&
          grid[j][t] !== "." &&
          grid[i][t] === grid[j][t]
        ) {
          return false;
        }
      }
      // 3X3 Box
      let row = Math.floor(i / 3) * 3;
      let col = Math.floor(t / 3) * 3;
      for (let x = row; x < row + 3; x++) {
        for (y = col; y < col + 2; y++) {
          if (
            x !== i &&
            y !== t &&
            grid[x][y] === grid[i][t] &&
            grid[x][y] !== "."
          ) {
            return false;
          }
        }
      }
    }
  }
  return true;
}
